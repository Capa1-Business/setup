document.addEventListener('DOMContentLoaded', () => {
  const root = document.documentElement;
  const themePill = document.getElementById("themePill");
  const themeToggle = document.getElementById("themeToggle");
  const languageSwitcher = document.getElementById('language-switcher');
  const yearSpan = document.getElementById("year");

  // Theme logic
  function setTheme(theme) {
    root.setAttribute("data-theme", theme);
    if (themePill) { // Check if themePill exists before trying to update its text
      themePill.textContent = theme === "dark" ? translations[root.lang].themeDark : translations[root.lang].themeLight;
    }
    localStorage.setItem("capa_theme", theme);
  }

  const savedTheme = localStorage.getItem("capa_theme");
  if (savedTheme === "dark" || savedTheme === "light") {
    setTheme(savedTheme);
  } else if (window.matchMedia && window.matchMedia('(prefers-color-scheme: dark)').matches) {
    setTheme('dark');
  } else {
    setTheme('light');
  }

  themeToggle.addEventListener("click", () => {
    const cur = root.getAttribute("data-theme") || "light";
    setTheme(cur === "dark" ? "light" : "dark");
  });

  // Language logic
  const setLanguage = (lang) => {
    root.lang = lang; // Set the lang attribute on the html element
    document.querySelectorAll('[data-i18n]').forEach(element => {
      const key = element.getAttribute('data-i18n');
      if (translations[lang] && translations[lang][key]) {
        if (element.tagName === 'TITLE') {
          element.textContent = translations[lang][key];
        } else if (element.tagName === 'INPUT' && element.type === 'submit') {
          element.value = translations[lang][key];
        } else if (element.tagName === 'META' && element.name === 'description') {
          element.content = translations[lang][key];
        }
        else {
          element.innerHTML = translations[lang][key]; // Use innerHTML for cases like 'reviewer1' with <span>
        }
      }
    });

    // Update legal page links
    const legalLinks = document.querySelectorAll('.footer-nav a');
    legalLinks.forEach(link => {
      const originalHref = link.getAttribute('href').split('?')[0]; // Remove existing query params
      link.setAttribute('href', `${originalHref}?lang=${lang}`);
    });

    // Update theme pill text after language change
    const currentTheme = root.getAttribute("data-theme") || "light";
    if (themePill) {
      themePill.textContent = currentTheme === "dark" ? translations[lang].themeDark : translations[lang].themeLight;
    }
    
    localStorage.setItem('lang', lang);
  };

  // Initialize language
  const urlParams = new URLSearchParams(window.location.search);
  const paramLang = urlParams.get('lang');
  const savedLang = localStorage.getItem('lang') || navigator.language.split('-')[0] || 'en';
  const initialLang = paramLang || savedLang;

  languageSwitcher.value = initialLang;
  setLanguage(initialLang);

  languageSwitcher.addEventListener('change', (event) => {
    setLanguage(event.target.value);
  });

  // Set current year in footer
  if (yearSpan) {
    yearSpan.textContent = new Date().getFullYear();
  }
});